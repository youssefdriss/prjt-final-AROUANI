// Function to toggle vertical navbar visibility
function toggleVerticalNavbar() {
    var verticalNavbar = document.getElementById("verticalNavbar");
    verticalNavbar.classList.toggle("active");
  }
  
  // Function to toggle horizontal navbar visibility
  function toggleHorizontalNavbar() {
    var horizontalNavbar = document.getElementById("horizontalNavbar");
    horizontalNavbar.classList.toggle("active");
  }
  
  // Function to toggle vertical column visibility
  function toggleVerticalColumn() {
    var verticalColumn = document.getElementById("verticalColumn");
    verticalColumn.classList.toggle("active");
  }
 